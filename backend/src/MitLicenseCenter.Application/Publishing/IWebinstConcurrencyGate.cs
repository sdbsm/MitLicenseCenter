namespace MitLicenseCenter.Application.Publishing;

/// <summary>
/// Ограничитель числа одновременных спавнов <c>webinst.exe</c>. Гарантирует, что в
/// любой момент времени запущено не больше фиксированного числа процессов webinst —
/// независимо от того, сколько публикаций оператор запустил пачкой (MLC-046) и сколько
/// операторов работают параллельно.
/// </summary>
/// <remarks>
/// MLC-046. Массовая публикация исполняется фронтом как N идемпотентных одиночных
/// вызовов <c>POST /publications/{id}/publish</c>; параллелизм клиент держит малым, но
/// клиентский лимит — не граница безопасности. Реальный кэп на спавн-бюджет (семья
/// ADR-3.3) держит этот замок в адаптере <see cref="IWebinstPublisher"/>: single-node,
/// один процесс → достаточно in-process <c>SemaphoreSlim(N, N)</c>. На одиночную
/// публикацию поведение не меняется (свободный слот берётся мгновенно).
/// </remarks>
public interface IWebinstConcurrencyGate
{
    /// <summary>
    /// Асинхронно берёт слот. Возвращает scope, освобождающий слот при <c>Dispose</c>
    /// (использовать с <c>using</c>). Если все слоты заняты — ожидает освобождения.
    /// </summary>
    Task<IDisposable> AcquireAsync(CancellationToken ct);
}
