namespace MitLicenseCenter.Application.TechLog;

// Анализатор долгих запросов к СУБД из событий DBMSSQL (MLC-234, этап B трека
// «Расследование производительности»). Строит из потока TechLogEvent топ долгих
// запросов и агрегаты группировки по похожим запросам.
//
// ГРАНИЦА: ТОЛЬКО события DBMSSQL. Другие события (TLOCK, SDBL, EXCP и др.) игнорируются.
//
// Ключевые факты стенда (40_TECHLOG §6): фильтр по длительности в logcfg НЕ работает
// для JSON-ТЖ 8.5 (ловит 0). Порог применяет ЭТОТ анализатор (параметр метода),
// а НЕ logcfg. Объём режется типом события и p:processName в logcfg (оба работают).
//
// Архитектура: чистый C# без файловой системы и БД (как ILockTreeAnalyzer/ITechLogParser).
// Потребляет IEnumerable<TechLogEvent> — не знает про файлы/JSON/диск. Stateless → singleton.
// Устойчивость (40_TECHLOG §7): «поля-призраки» (Sql иногда отсутствует, набор полей
// варьируется), дубли ключей, варианты имён — анализатор НИКОГДА не бросает на любом входе.
public interface ISlowQueryAnalyzer
{
    // Анализирует поток событий ТЖ: отбирает DBMSSQL с длительностью ≥ порога,
    // строит топ и группировку по похожим запросам.
    //
    // thresholdMicroseconds — порог длительности в МИКРОСЕКУНДАХ (единица ТЖ 8.5,
    //   40_TECHLOG §4). Запросы короче порога отсекаются. Дефолт 1 000 000 µs = 1 секунда.
    //   Рекомендация: для расследований 5 000 000 µs (5 с) или выше.
    //
    // topN — максимальное число записей в TopQueries (результирующий топ по длительности
    //   убывающим). Дефолт 50.
    //
    // Не бросает исключений на любом входе (принцип never-throws слоя ТЖ).
    SlowQueryAnalysisResult Analyze(
        IEnumerable<TechLogEvent> events,
        long thresholdMicroseconds = 1_000_000,
        int topN = 50);
}
