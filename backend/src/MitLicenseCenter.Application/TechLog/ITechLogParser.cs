namespace MitLicenseCenter.Application.TechLog;

// Построчный парсер технологического журнала 1С 8.5 (JSON-режим) — ядро этапа B трека
// «Расследование производительности» (MLC-232). Чистый C# без файловой системы (как ILogcfgBuilder):
// принимает строки/TextReader, максимально юнит-тестируем. Чтение файлов/каталога с диска — задача
// этапа C (тонкий Infrastructure-хелпер поверх этого ядра).
//
// Контракт устойчивости (ЗАКОН по фактам стенда MLC-229, 40_TECHLOG §4/§7) — нарушать нельзя:
//   • вход — NDJSON: один JSON-объект на строку (НЕ массив [...]); читаем построчно (память не растёт);
//   • файл начинается с BOM (\xEF\xBB\xBF) — снимаем; кодировка UTF-8;
//   • событие — плоский объект, все значения строки; ключи могут дублироваться → сохраняем ВСЕ вхождения;
//   • парсер НИКОГДА не бросает: пустые строки пропускает, невалидный JSON в строке пропускает
//     (вызывающий код видит деградацию через счётчик SkippedLines в результате ParseLines),
//     а не валит весь файл (тот же принцип, что и у остального слоя ТЖ).
public interface ITechLogParser
{
    // Разбирает ОДНУ NDJSON-строку в событие. Возвращает null, если строка пустая/только пробелы или
    // содержит невалидный JSON либо не объект (толерантность — не бросаем). BOM в начале строки снимается.
    TechLogEvent? ParseLine(string? line);

    // Ленивый потоковый разбор последовательности строк: каждая валидная строка → событие; пустые и
    // битые строки пропускаются (их число — в SkippedLines). Перечисление ленивое — память не растёт
    // на больших журналах (NDJSON, 40_TECHLOG §4).
    TechLogParseResult ParseLines(IEnumerable<string> lines);

    // То же, но из TextReader (например, StreamReader поверх файла, открытого в Infrastructure). Ленивый
    // потоковый разбор; читатель не закрывается парсером (владение остаётся за вызывающим кодом).
    TechLogParseResult ParseReader(TextReader reader);
}
