namespace MitLicenseCenter.Application.TechLog;

// Анализатор исключений платформы 1С из событий EXCP (MLC-235, этап B трека
// «Расследование производительности»). Строит из потока TechLogEvent топ исключений
// по частоте с группировкой по типу и нормализованному тексту описания.
//
// ГРАНИЦА: ТОЛЬКО события EXCP. Прочие (DBMSSQL/TLOCK/SDBL/CALL) молча игнорируются.
//
// Особенность (40_TECHLOG §7): дедлок СУБД пишет ДВА EXCP с Exception=DataBaseException
// — не трактовать каждый EXCP как самостоятельный инцидент вслепую. DataBaseException
// выделяется флагом IsDatabaseException. Точная пара-корреляция дедлоков — после
// подтверждения на стенде; в этой версии частота DataBaseException может удваивать
// реальное число инцидентов блокировок/дедлоков СУБД (честно задокументировано).
//
// Устойчивость (40_TECHLOG §7): EXCP вездесущ (ложные при окне авторизации); поля-призраки
// (Descr/Context могут отсутствовать); дубли ключей. Анализатор НИКОГДА не бросает.
//
// Архитектура: чистый C# без ФС/БД. Stateless → singleton.
public interface IExceptionAnalyzer
{
    // Анализирует поток событий ТЖ: отбирает EXCP, группирует по типу исключения +
    // нормализованному тексту Descr, строит топ по частоте убывающим.
    //
    // topN — максимальное число групп в TopExceptions. Дефолт 50.
    //   При topN <= 0 возвращает все группы.
    //
    // Не бросает исключений на любом входе (принцип never-throws слоя ТЖ).
    ExceptionAnalysisResult Analyze(
        IEnumerable<TechLogEvent> events,
        int topN = 50);
}
